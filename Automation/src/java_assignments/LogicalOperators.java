package java_assignments;

public class LogicalOperators {

	public static void main(String[] args) {
		boolean condition1 = true;
		boolean condition2 = false;

		//Logical AND
		System.out.println("condition1 && condition2 =" + (condition1 && condition2));

		//Logical OR
		System.out.println("condition1 || condition2 =" + (condition1 || condition2));

		//Logical AND NOT
		System.out.println("condition1 && condition2 =" + !(condition1 && condition2));

		//Logical OR NOT
		System.out.println("condition1 || condition2 =" + !(condition1 || condition2));
	}



}
